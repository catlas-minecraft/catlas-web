CREATE INDEX IF NOT EXISTS nodes_geom_gist_idx
  ON core.nodes
  USING gist (geom);

CREATE INDEX IF NOT EXISTS way_nodes_way_id_seq_idx
  ON core.way_nodes (way_id, seq);

CREATE INDEX IF NOT EXISTS way_nodes_node_id_idx
  ON core.way_nodes (node_id);

CREATE INDEX IF NOT EXISTS relation_members_relation_id_seq_idx
  ON core.relation_members (relation_id, seq);

CREATE INDEX IF NOT EXISTS way_geometries_geom_gist_idx
  ON derived.way_geometries
  USING gist (geom);

CREATE INDEX IF NOT EXISTS relation_geometries_geom_gist_idx
  ON derived.relation_geometries
  USING gist (geom);
